//------------------------------------------
//		2017.2.18 by jianfeng
//	定时器2中断，在中断向量表中,可以查到
//------------------------------------------
void TIM2_IRQHandler(void) 
{
	//当发生TIM2中断的时候
	if (TIM_GetITStatus(TIM2, TIM_IT_Update) != RESET)  
	{
		//中断处理
		static int a = 0;
		if(a == 0)
		{
			a = 1;
			LED_OFF();
		}
		else
		{
			a = 0;
			LED_ON();
			sendflag = 1;
		}
	}
	
	//清除中断标志
	TIM_ClearITPendingBit(TIM2, TIM_IT_Update); 
}

#endif

